# PNG file header bytes
png_bytes = [
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
    0x00, 0x00, 0x00, 0x0D,  # IHDR chunk length
    0x49, 0x48, 0x44, 0x52,  # IHDR chunk type
    0x00, 0x00, 0x00, 0x10,  # Width: 16
    0x00, 0x00, 0x00, 0x10,  # Height: 16
    0x08,  # Bit depth: 8
    0x02,  # Color type: Truecolor
    0x00,  # Compression method
    0x00,  # Filter method
    0x00,  # Interlace method
    0x90, 0x77, 0x53, 0xDE,  # CRC for IHDR
    0x00, 0x00, 0x00, 0x0A,  # IDAT chunk length
    0x49, 0x44, 0x41, 0x54,  # IDAT chunk type
    0x78, 0x9C, 0x63, 0x60, 0x60, 0x60, 0x00, 0x00,  # Compressed data
    0x00, 0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4,  # CRC for IDAT
    0x00, 0x00, 0x00, 0x00,  # IEND chunk length
    0x49, 0x45, 0x4E, 0x44,  # IEND chunk type
    0xAE, 0x42, 0x60, 0x82   # CRC for IEND
]

# Write the bytes to a file
with open("invalid.png", "wb") as f:
    f.write(bytearray(png_bytes))